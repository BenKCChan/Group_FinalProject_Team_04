    private void loadInventory() {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblInventory.getModel();
    model.setRowCount(0);

    Business.Inventory.OilInventory inv = organization.getOilInventory();
    for (Business.Inventory.OilInventory.OilInventoryItem item : inv.getItems()) {
        model.addRow(new Object[]{
            item.getItemId(),
            item.getSupplier(),
            item.getQuantityBarrels(),
            String.format("$%.2f", item.getPricePerBarrel()),
            String.format("$%.2f", item.getTotalCost()),
            item.getPurchaseDate(),
            item.getLinkedRequestId()
        });
    }

    lblTotalBarrels.setText("Total Barrels: " + inv.getTotalBarrels());
    lblTotalCost.setText("Total Cost: $" + String.format("%.2f", inv.getTotalCost()));
    lblAvgPrice.setText("Avg Price/Barrel: $" + String.format("%.2f", inv.getAveragePricePerBarrel()));
}
    

