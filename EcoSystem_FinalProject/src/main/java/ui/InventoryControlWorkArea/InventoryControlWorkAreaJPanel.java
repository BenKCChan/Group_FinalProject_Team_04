    private void btnSubmitPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitPurchaseActionPerformed
        try {
         String reqId = txtReqId.getText().trim();
            String supplier = txtSupplier.getText().trim();
            int qty = Integer.parseInt(txtQty.getText().trim());
            double price = Double.parseDouble(txtPrice.getText().trim());

            if (supplier.isEmpty() || qty <= 0 || price <= 0) {
            purchaseFeedbackLabel.setText("Please fill in all fields with valid values.");
            purchaseFeedbackLabel.setForeground(java.awt.Color.RED);
            return;
        }

            organization.getOilInventory().addPurchase(supplier, qty, price, reqId);

            purchaseFeedbackLabel.setText("Purchase recorded successfully!");
            purchaseFeedbackLabel.setForeground(java.awt.Color.GREEN);
            txtReqId.setText("");
            txtSupplier.setText("");
            txtQty.setText("");
            txtPrice.setText("");

    } catch (NumberFormatException e) {
        purchaseFeedbackLabel.setText("Please enter valid numbers for quantity and price.");
        purchaseFeedbackLabel.setForeground(java.awt.Color.RED);
    }
    }//GEN-LAST:event_btnSubmitPurchaseActionPerformed

