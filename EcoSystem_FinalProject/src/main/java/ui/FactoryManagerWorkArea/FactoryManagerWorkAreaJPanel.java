    private void loadPendingRequests() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblPending.getModel();
        model.setRowCount(0);
        
        Business.OilTrade.OilTradeRequestList list = system.getNetworkList().get(0).getTradeRequestList();
        for (Business.OilTrade.OilTradeRequest req : list.getRequestsForEnterprise(enterprise.getName())) {
                if (req.getStatus() == Business.OilTrade.OilTradeRequest.RequestStatus.PENDING) {
                        model.addRow(new Object[]{
                    req.getRequestId(), req.getRequestType(), req.getFromOrganization(), String.format("$%.2f", req.getPricePerBarrel()), req.getQuantityBarrels(), req.getRequestedBy(), req.getStatus()
                });
            }
        }
    }
    private void loadAllRequests() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblAllRequests.getModel();
        model.setRowCount(0);
        
        Business.OilTrade.OilTradeRequestList list = system.getNetworkList().get(0).getTradeRequestList();
        for (Business.OilTrade.OilTradeRequest req : list.getRequestsForEnterprise(enterprise.getName())) {
            if (req.getStatus() == Business.OilTrade.OilTradeRequest.RequestStatus.PENDING) {
                model.addRow(new Object[]{
                    req.getRequestId(), req.getRequestType(), req.getFromOrganization(), String.format("$%.2f", req.getPricePerBarrel()), req.getQuantityBarrels(), req.getRequestedBy(), req.getStatus()
                });
        }
    }
    }
    private Business.OilTrade.OilTradeRequest getSelectedRequest() {
            int row = tblPending.getSelectedRow();
            if(row < 0) return null;
            String requestId = (String) tblPending.getValueAt (row, 0);
            return system.getNetworkList().get(0).getTradeRequestList().findRequest(requestId);
}

