    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        Business.OilTrade.OilTradeRequest req = getSelectedRequest();
        if (req == null) {
            lblFeedback.setText("Please select a request first");
            lblFeedback.setForeground(Color.red);
            return;
        }
        req.setStatus(Business.OilTrade.OilTradeRequest.RequestStatus.APPROVED);
        req.setReviewNotes(txtNotes.getText().trim());
        lblFeedback.setText("Request " + req.getRequestId() + " approved.");
        lblFeedback.setForeground(Color.GREEN);
        txtNotes.setText("");
        loadPendingRequests();
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        Business.OilTrade.OilTradeRequest req = getSelectedRequest();
        if (req == null) {
            lblFeedback.setText("Please select a request first");
            lblFeedback.setForeground(Color.red);
            return;
        }
        req.setStatus(Business.OilTrade.OilTradeRequest.RequestStatus.REJECTED);
        req.setReviewNotes(txtNotes.getText().trim());
        lblFeedback.setText("Request " + req.getRequestId() + " rejected.");
        lblFeedback.setForeground(Color.GREEN);
        txtNotes.setText("");
        loadPendingRequests();
    }//GEN-LAST:event_btnRejectActionPerformed

